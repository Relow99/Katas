
public class evenOrOdd {

    public static void evenOrOdd(int number) {

        if ( number %2==0) {
            System.out.println("Entered number is even");
        }
        else {
            System.out.println("Entered number is odd");
        }

    }

    public static void main(String args[]) {

        evenOrOdd(5);

}

}
